class DetallePersona {
  public static void main(String[] args) {
    // Nombre completo
    String nombreCompleto = "Alan Alvarez";

    // Edad
    int edad = 24;

    // Altura
    double altura = 1.81;

    // Pais de origen
    String pais = "Argentina";

    // Casado(C) o Soltero(S)
    char estadoCivil = 'S';

    // Imprimir valores
    System.out.println("Nombre completo:"+nombreCompleto+
        "\n Edad:"+edad+"\n Altura:"+altura+"\n Pais:"+pais+"\n Estado civil:"+estadoCivil);
  }
}
